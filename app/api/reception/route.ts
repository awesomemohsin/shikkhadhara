import { NextRequest, NextResponse } from 'next/server';
import { VisitorLog } from '@/lib/models';
import { getTenantContext } from '@/lib/tenant-context';
import { TenantQuery } from '@/lib/db/tenant-query';
import { validateRequestAccess, Role } from '@/lib/auth/rbac';
import { verifyToken } from '@/lib/auth-utils';

export async function GET(request: NextRequest) {
  try {
    const tenantContext = await getTenantContext(request);
    if (!tenantContext) {
      return NextResponse.json({ message: 'Tenant context unresolved' }, { status: 400 });
    }

    const headerTenantId = request.headers.get('x-tenant-id') || tenantContext.tenantId;
    const logs = await TenantQuery.find(VisitorLog, headerTenantId, {}, null, { sort: { createdAt: -1 } });

    const visitors = logs.filter((l: any) => l.type === 'visitor');
    const enquiries = logs.filter((l: any) => l.type === 'enquiry');
    const calls = logs.filter((l: any) => l.type === 'call');

    return NextResponse.json({ visitors, enquiries, calls });
  } catch (error: any) {
    return NextResponse.json({ message: error.message }, { status: 500 });
  }
}

export async function POST(request: NextRequest) {
  try {
    const tenantContext = await getTenantContext(request);
    if (!tenantContext) {
      return NextResponse.json({ message: 'Tenant context unresolved' }, { status: 400 });
    }

    const headerTenantId = request.headers.get('x-tenant-id') || tenantContext.tenantId;
    const authHeader = request.headers.get('authorization');
    if (!authHeader) {
      return NextResponse.json({ message: 'Unauthorized: Token missing' }, { status: 401 });
    }

    const token = authHeader.replace('Bearer ', '');
    const decoded = verifyToken(token);
    if (!decoded) {
      return NextResponse.json({ message: 'Unauthorized: Invalid token' }, { status: 401 });
    }

    const { role, tenantId: tokenTenantId, organizationId: tokenOrgId } = decoded;
    const userTenantId = tokenTenantId || tokenOrgId;

    if (!validateRequestAccess(role, userTenantId, Role.STAFF, headerTenantId)) {
      return NextResponse.json({ message: 'Forbidden: Insufficient privileges' }, { status: 403 });
    }

    const data = await request.json();
    if (!data.type) {
      return NextResponse.json({ message: 'Type is required' }, { status: 400 });
    }

    const log = await TenantQuery.create(VisitorLog, headerTenantId, {
      type: data.type,
      name: data.name,
      phone: data.phone,
      purpose: data.purpose,
      checkIn: data.checkIn,
      status: data.status || 'checked_in',
      guardianName: data.guardianName,
      class: data.class,
      date: data.date,
      caller: data.caller,
      notes: data.notes,
    });

    return NextResponse.json({ log }, { status: 201 });
  } catch (error: any) {
    return NextResponse.json({ message: error.message }, { status: 500 });
  }
}

